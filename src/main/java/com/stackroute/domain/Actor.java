package com.stackroute.domain;

public class Actor {

    //Variables
    private String name;
    private String gender;
    private int age;

    //constructor
    public Actor() {
    }

    //setters
    public void setName(String name) {
        this.name = name;
    }

    public void setGender(String gender) {
        this.gender = gender;
    }

    public void setAge(int age) {
        this.age = age;
    }

    //getters
    public String getName() {
        return name;
    }

    public String getGender() {
        return gender;
    }

    public int getAge() {
        return age;
    }


} 


